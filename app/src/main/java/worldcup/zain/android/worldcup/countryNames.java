public class countryNames {

    private int mImage;
    private String mCountryName;


    public countryNames(int mImage, String mCountryName) {
        this.mImage = mImage;
        this.mCountryName = mCountryName;
    }

    public int getmImage() {
        return mImage;
    }

    public String getmCountryName() {
        return mCountryName;
    }
}
